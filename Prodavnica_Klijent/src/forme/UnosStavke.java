    private void srediCMB() {
        
        ArrayList<Artikal> lista = KontrolerKI.getInstance().vratiArtikle();
        cmbArtikal.removeAllItems();
        
        for(Artikal a: lista) {
            cmbArtikal.addItem(a);
        }
        
        if(s.getNarudzbinaid()!=0) {
            cmbArtikal.setSelectedItem(s.getArtikal());
            txtKolicina.setText(Integer.toString(s.getKolicina()));
        }
        
    }
