    private void btnPotvrdiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPotvrdiActionPerformed
       Artikal a = (Artikal) cmbArtikal.getSelectedItem();
       if(txtKolicina.getText().isEmpty()) {
           JOptionPane.showMessageDialog(this, "Unesite kolicinu");
           return;
       }
       try {
           int provera = Integer.parseInt(txtKolicina.getText());
       } catch(NumberFormatException e) {
           JOptionPane.showMessageDialog(this, "Morate uneti ceo broj");
           return;
       }
       int kolicina = Integer.parseInt(txtKolicina.getText());
       s.setArtikal(a);
       s.setKolicina(kolicina);
       s.setIznos((a.getCenaBezporeza()+a.getCenaBezporeza()*a.getPs().getIznosStope())*kolicina);
       this.dispose();
    }//GEN-LAST:event_btnPotvrdiActionPerformed

