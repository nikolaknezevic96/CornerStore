    private void btnIzmeniStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniStavkuActionPerformed
        int i = tblStavka.getSelectedRow();
        if(i==-1) {
            JOptionPane.showMessageDialog(this, "Izaberite stavku za izmenu");
            return;
        }
        ModelTabeleStavka mts = (ModelTabeleStavka) tblStavka.getModel();
        ArrayList<StavkaNarudzbine> lista = mts.getLista();
        StavkaNarudzbine s = lista.get(i);
        UnosStavke us = new UnosStavke(nf, rootPaneCheckingEnabled, s);
        us.setVisible(true);
        
        StavkaNarudzbine novaStavka = us.getS();
        lista.set(i, novaStavka);
        mts.setLista(lista);
        tblStavka.setModel(mts);
    }//GEN-LAST:event_btnIzmeniStavkuActionPerformed

