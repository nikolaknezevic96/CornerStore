    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        UnosStavke us = new UnosStavke(this, rootPaneCheckingEnabled);
        us.setVisible(true);
        
        StavkaNarudzbine s = us.getS();
        s.setNarudzbinaid(n.getNarudzbinaid());
        
        ModelTabeleStavka mts = (ModelTabeleStavka) tblStavka.getModel();
        ArrayList<StavkaNarudzbine> lista = mts.getLista();
        int i = 0;
        for(StavkaNarudzbine sn : lista) {
            if(s.getArtikal().getArtikalID() == sn.getArtikal().getArtikalID()) {
               int kolicina = s.getKolicina();
               lista.get(i).setKolicina(lista.get(i).getKolicina()+kolicina);
               tblStavka.setModel(mts);
               mts.osvezi();
               return;
            }
            i++;
        }
        mts.dodaj(s);
        tblStavka.setModel(mts);
    }//GEN-LAST:event_btnDodajActionPerformed

